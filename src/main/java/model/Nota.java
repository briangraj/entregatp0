package model;

public class Nota {

	private String id;
	private String value;
	private String created_at;
	private String updated_at;
	public String getId() {
		return id;
	}
	public String getValue() {
		return value;
	}
	public String getCreated_at() {
		return created_at;
	}
	public String getUpdated_at() {
		return updated_at;
	}
	
	
}
